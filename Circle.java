public class Circle {
	double radius;
	
	public Circle() {
		this.radius = radius;
	}
	
	public Circle(double radius) {
		this.radius = radius;
	}
	
	public void area() {
		double area= Math.PI*(Math.pow(radius, 2));
		System.out.println(area);
		
	}
	
	
}
